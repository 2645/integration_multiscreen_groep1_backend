package be.ehb.restservermetdatabase.model;

/**
 *
 * @author janhd
 */
public class Friendship {
    private int id, fromId, toId;
    
    public Friendship(){
        
    }

    public Friendship(int id, int fromId, int toId) {
        this.id = id;
        this.fromId = fromId;
        this.toId = toId;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getFromId() {
        return fromId;
    }

    public void setFromId(int fromId) {
        this.fromId = fromId;
    }

    public int getToId() {
        return toId;
    }

    public void setToId(int toId) {
        this.toId = toId;
    }

}
