package be.ehb.restservermetdatabase.model;

/**
 *
 * @author janhd
 */
public class Friends {
    public int friend_id, user_id1, user_id2;

    public Friends(int friend_id, int user_id1, int user_id2) {
        this.friend_id = friend_id;
        this.user_id1 = user_id1;
        this.user_id2 = user_id2;
    }

    public int getFriend_id() {
        return friend_id;
    }

    public void setFriend_id(int friend_id) {
        this.friend_id = friend_id;
    }

    public int getUser_id1() {
        return user_id1;
    }

    public void setUser_id1(int user_id1) {
        this.user_id1 = user_id1;
    }

    public int getUser_id2() {
        return user_id2;
    }

    public void setUser_id2(int user_id2) {
        this.user_id2 = user_id2;
    }
    
}
